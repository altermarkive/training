// https://leetcode.com/problems/course-schedule/
// #medium

use std::collections::{HashMap, HashSet, VecDeque};

struct State {
    node: i32,
    ancestors: HashSet<i32>,
}

pub struct Solution;

impl Solution {
    fn dfs(graph: HashMap<i32, HashSet<i32>>, node_value: i32, visited: &mut HashSet<i32>) -> bool {
        let mut node = node_value;
        let mut queue = VecDeque::new();
        queue.push_back(State {
            node,
            ancestors: HashSet::new(),
        });
        while !queue.is_empty() {
            let state = queue.pop_front().unwrap();
            node = state.node;
            if state.ancestors.contains(&node) {
                return false;
            }
            if !visited.contains(&node) {
                visited.insert(node);
                if let Some(adjacent) = graph.get(&node) {
                    for other in adjacent {
                        let mut ancestors = state.ancestors.clone();
                        ancestors.insert(node);
                        queue.push_back(State {
                            node: *other,
                            ancestors,
                        });
                    }
                }
            }
        }
        true
    }

    pub fn can_finish(_num_courses: i32, prerequisites: Vec<Vec<i32>>) -> bool {
        let mut graph = HashMap::new();
        for prerequisite in prerequisites {
            graph
                .entry(prerequisite[1])
                .or_insert_with(HashSet::new)
                .insert(prerequisite[0]);
        }
        let mut visited = HashSet::new();
        for start in graph.keys() {
            if !Self::dfs(graph.clone(), *start, &mut visited) {
                return false;
            }
        }
        true
    }
}

